function Vertex2d(xpos, ypos){
	this.x = xpos;
	this.y = ypos;
}

function Vertex3d(xpos, ypos, zpos){
	this.x = xpos;
	this.y = ypos;
	this.z = zpos;
}

function Position(xpos, ypos, zpos){
	this.x = xpos;
	this.y = ypos;
	this.z = zpos;
}

function Rotation(xrot, yrot, zrot){
	this.x = xrot;
	this.y = yrot;
	this.z = zrot;
}
